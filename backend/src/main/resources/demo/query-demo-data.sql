insert into fact_oil_fume_warning(
    warning_date, street_code, region_code, warning_level,
    warning_count, unclosed_count, avg_concentration, max_concentration
)
select
    warning_date,
    street_code,
    region_code,
    warning_level,
    sum(warning_count) as warning_count,
    sum(unclosed_count) as unclosed_count,
    round(sum(avg_concentration * warning_count) / nullif(sum(warning_count), 0), 2) as avg_concentration,
    max(max_concentration) as max_concentration
from fact_oil_fume_warning_event
group by warning_date, street_code, region_code, warning_level;

